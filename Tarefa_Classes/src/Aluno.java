
public class Aluno {

    private int nota;
    private String nome;

    private String materia;

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }

    public String getNome() {

        return nome;
    }

    public void setNome(String nome) {

        this.nome = nome;
    }

    public String getMateria() {

        return materia;
    }

    public void setMateria(String materia) {

        this.materia = materia;
    }
}
